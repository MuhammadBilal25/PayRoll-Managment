DELIMITER //
Drop function CalculateNetSalary;
CREATE FUNCTION CalculateNetSalary(emp_id INT, salary_month VARCHAR(10))
RETURNS DECIMAL(10,2)
DETERMINISTIC
BEGIN
    DECLARE basic_salary DECIMAL(10,2);
    DECLARE bonus DECIMAL(10,2);
    Declare Deduction Decimal (10,2);
    DECLARE net_salary DECIMAL(10,2);
    
 
    SELECT 
        CASE salary_month
            WHEN 'January' THEN January
            WHEN 'February' THEN February
            WHEN 'March' THEN March
            WHEN 'April' THEN April
            WHEN 'May' THEN May
            WHEN 'June' THEN June
            WHEN 'July' THEN July
            WHEN 'August' THEN August
            WHEN 'September' THEN September
            WHEN 'October' THEN October
            WHEN 'November' THEN November
            WHEN 'December' THEN December
        END 
    INTO basic_salary
    FROM Salary
    WHERE Employee_id = emp_id;

    -- Get the bonus
    SELECT 
        CASE salary_month
            WHEN 'January' THEN January
            WHEN 'February' THEN February
            WHEN 'March' THEN March
            WHEN 'April' THEN April
            WHEN 'May' THEN May
            WHEN 'June' THEN June
            WHEN 'July' THEN July
            WHEN 'August' THEN August
            WHEN 'September' THEN September
            WHEN 'October' THEN October
            WHEN 'November' THEN November
            WHEN 'December' THEN December
        END 
    INTO bonus
    FROM Bonus
    WHERE Employee_id = emp_id;
    
    SELECT 
        CASE salary_month
            WHEN 'January' THEN January
            WHEN 'February' THEN February
            WHEN 'March' THEN March
            WHEN 'April' THEN April
            WHEN 'May' THEN May
            WHEN 'June' THEN June
            WHEN 'July' THEN July
            WHEN 'August' THEN August
            WHEN 'September' THEN September
            WHEN 'October' THEN October
            WHEN 'November' THEN November
            WHEN 'December' THEN December
        END 
    INTO Deduction
    FROM Deduction
    WHERE Employee_id = emp_id;

    -- Calculate the net salary
    SET net_salary = basic_salary + bonus- Deduction;

    RETURN net_salary;
END //

